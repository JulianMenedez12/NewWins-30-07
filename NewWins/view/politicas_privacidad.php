            <h1 class="section-title">Política de Privacidad</h1>
            <p>En NewWins, respetamos tu privacidad y estamos comprometidos a proteger tus datos personales. Esta Política de Privacidad explica cómo recolectamos, usamos, compartimos y protegemos tu información personal cuando utilizas nuestro sitio web.</p>
            
            <h2 class="section-title">1. Información que Recopilamos</h2>
            <p>Recopilamos diferentes tipos de información para ofrecerte un mejor servicio, entre ellos:</p>
            <ul>
                <li><strong>Información Personal:</strong> Incluye tu nombre, dirección de correo electrónico, y cualquier otra información que proporcionas al registrarte o al interactuar con nuestro sitio.</li>
                <li><strong>Información de Uso:</strong> Datos sobre cómo utilizas nuestro sitio, como tu dirección IP, tipo de navegador, páginas visitadas y duración de la visita.</li>
                <li><strong>Cookies y Tecnologías Similares:</strong> Utilizamos cookies para mejorar tu experiencia en nuestro sitio y analizar el tráfico.</li>
            </ul>
            
            <h2 class="section-title">2. Cómo Usamos Tu Información</h2>
            <p>Utilizamos tu información para los siguientes propósitos:</p>
            <ul>
                <li><strong>Proporcionar Servicios:</strong> Para ofrecerte noticias, artículos y otros contenidos de tu interés.</li>
                <li><strong>Mejorar el Sitio:</strong> Para analizar el uso del sitio y mejorar nuestras ofertas y funcionalidades.</li>
                <li><strong>Comunicación:</strong> Para enviarte actualizaciones, boletines informativos, y responder a tus consultas.</li>
                <li><strong>Seguridad:</strong> Para garantizar la seguridad del sitio y prevenir actividades fraudulentas.</li>
            </ul>
            
            <h2 class="section-title">3. Compartición de Información</h2>
            <p>No vendemos, intercambiamos ni transferimos tu información personal a terceros sin tu consentimiento, excepto en los siguientes casos:</p>
            <ul>
                <li><strong>Proveedores de Servicios:</strong> Compartimos información con proveedores que nos ayudan a operar el sitio web y prestar servicios, bajo estrictas condiciones de confidencialidad.</li>
                <li><strong>Requisitos Legales:</strong> Podemos divulgar información si así lo requiere la ley, para cumplir con procesos legales o proteger nuestros derechos.</li>
            </ul>
            
            <h2 class="section-title">4. Seguridad de los Datos</h2>
            <p>Implementamos medidas de seguridad técnicas y organizativas para proteger tu información personal contra acceso no autorizado, divulgación, alteración o destrucción. Sin embargo, ninguna transmisión de datos a través de Internet puede garantizarse al 100% como segura.</p>
            
            <h2 class="section-title">5. Tus Derechos</h2>
            <p>Tienes derecho a acceder, corregir o eliminar tu información personal que poseemos. Puedes hacer esto contactándonos a través de la información de contacto proporcionada más abajo.</p>
            
            <h2 class="section-title">6. Cambios en la Política de Privacidad</h2>
            <p>Podemos actualizar esta Política de Privacidad ocasionalmente. Te notificaremos sobre cambios significativos mediante la publicación de la nueva política en nuestro sitio web. Te recomendamos que revises esta política periódicamente para estar informado sobre cómo protegemos tu información.</p>
            

